// (ENADE) O termo imagem designa uma função intensidade luminosa bidimensional f,
// em que um valor de intensidade é associado a coordenadas espaciais (x, y). Uma imagem
// digital é obtida pela digitalização das coordenadas espaciais por meio de um processo
// conhecido como amostragem da imagem. Dessa forma, uma imagem contínua
// monocromática f (x, y) é aproximada por amostras igualmente espaçadas, arranjadas na
// forma de uma matriz N×M, em que cada elemento é um valor inteiro g. O intervalo [Gmin,
// Gmax], do menor ao maior valor de intensidade g, é denominado escala de cinza.
// Normalmente, Gmin = 0 corresponde a preto, e Gmax = G corresponde ao branco.
// Considerando esses conceitos, assinale a opção correta.
// a. O processo de digitalização da imagem requer que as dimensões N e M da matriz
// mencionada acima sejam múltiplas do número de tons de cinza na imagem.
// b. Para imagens binárias, se L for o número de tons de cinza representáveis, e L = 2^k
// , então k = 2.
// c. Os métodos para realce de imagens que operam no domínio espacial fazem uso
// do conceito de vizinhança de pixel.
// d. Métodos de filtragem normalmente usam máscaras para impedir a transformação
// dos níveis de cinza dos pixels da imagem.
// e. Limiarização é um tipo de processamento de imagens que amplia o número de
// níveis de cinza da imagem.
